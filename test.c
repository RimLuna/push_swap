int	get_median(int *a, int len)
{
	int	i;
	int	j;
	int	upper;
	int	lower;

	j = 0;
	while (j < len)
	{
		upper = 0;
		lower = 0;
		i = 0;
		while (i < len)
		{
			if (a[i] < a[j])
				lower++;
			else if (a[i] > a[j])
				upper++;
			i++;
		}
		if (lower == upper || lower + 1 == upper)
			return (a[j]);
		j++;
	}
	return (0);
}

int     mid(int a, int b, int c)
{
    //printf("%d, %d, %d\n", a,b,c);
    if ((a < b && a > c) || (a > b && a < c))
        return (a);
    if ((b < a && b > c) || (b > a && b < c))
        return (b);
    return (c);
}

int     med(int *a, int len)
{
    int median;
    int i;

    i = 0;
    median = a[i];
    while (i < len - 2)
    {
        median = mid(median, a[i + 1], a[i + 2]);
        //printf("med = %d\n", median);
        i += 2;
    }
    return (median);
}

